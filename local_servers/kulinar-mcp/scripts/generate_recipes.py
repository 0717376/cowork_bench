def _slug(s: str) -> str:
    table = str.maketrans({
        "а": "a", "б": "b", "в": "v", "г": "g", "д": "d", "е": "e", "ё": "e",
        "ж": "zh", "з": "z", "и": "i", "й": "y", "к": "k", "л": "l", "м": "m",
        "н": "n", "о": "o", "п": "p", "р": "r", "с": "s", "т": "t", "у": "u",
        "ф": "f", "х": "h", "ц": "ts", "ч": "ch", "ш": "sh", "щ": "sch",
        "ъ": "", "ы": "y", "ь": "", "э": "e", "ю": "yu", "я": "ya",
    })
    s2 = s.lower().translate(table)
    s2 = re.sub(r"[^a-z0-9]+", "-", s2).strip("-")
    return s2


def _ingredient(name, qty, unit, notes=""):
    if qty is None and unit is None:
        text = f"- {name}" + (f" ({notes})" if notes else "")
    else:
        q = f"{qty} {unit}" if (qty is not None and unit) else (str(qty) if qty is not None else "")
        text = f"- {name} {q}".strip()
        if notes:
            text += f" — {notes}"
    return {
        "name": name,
        "quantity": qty,
        "unit": unit,
        "text_quantity": text,
        "notes": notes or "",
    }


def _recipe(name, category, desc, ings, steps, prep, cook, servings, difficulty, tags, notes=None):
    return {
        "id": f"{_slug(category)}-{_slug(name)}",
        "name": name,
        "description": desc,
        "source_path": "",
        "image_path": None,
        "category": category,
        "difficulty": difficulty,
        "tags": tags,
        "servings": servings,
        "ingredients": [_ingredient(*i) for i in ings],
        "steps": [{"step": idx + 1, "description": s} for idx, s in enumerate(steps)],
        "prep_time_minutes": prep,
        "cook_time_minutes": cook,
        "total_time_minutes": (prep or 0) + (cook or 0),
        "additional_notes": notes or [],
    }


