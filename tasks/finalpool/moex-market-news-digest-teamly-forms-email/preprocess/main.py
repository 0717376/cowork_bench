PUBLISH_ISO = "2026-05-26T09:00:00Z"


def clear_tables(conn):
    with conn.cursor() as cur:
        # gform.*
        for tbl in ("gform.responses", "gform.questions", "gform.forms"):
            try:
                cur.execute(f"DELETE FROM {tbl}")
            except Exception:
                conn.rollback()
        # teamly.* — пользовательские страницы/пространства (сидовых: 3 страницы, 2 пространства)
        try:
            cur.execute("DELETE FROM teamly.pages WHERE id > 3")
        except Exception:
            conn.rollback()
        try:
            cur.execute("DELETE FROM teamly.spaces WHERE id > 2")
        except Exception:
            conn.rollback()
        # email.*
        for tbl in ("email.attachments", "email.sent_log", "email.drafts", "email.messages"):
            try:
                cur.execute(f'DELETE FROM {tbl}')
            except Exception:
                conn.rollback()
    conn.commit()
    print("[preprocess] Очищены gform.*, teamly.* (пользовательские), email.*")


def seed_email_folders(conn):
    with conn.cursor() as cur:
        for name in ("INBOX", "Sent"):
            try:
                cur.execute(
                    "INSERT INTO email.folders (name, flags) VALUES (%s, %s) ON CONFLICT DO NOTHING",
                    (name, '["\\\\HasNoChildren"]'),
                )
            except Exception:
                conn.rollback()
    conn.commit()
    print("[preprocess] Засеяны папки email: INBOX, Sent")


def seed_news(conn):
    """Идемпотентно засевает русскоязычные новости-источники в moex.news."""
    with conn.cursor() as cur:
        symbols = sorted({row[0] for row in NEWS})
        cur.execute(
            "DELETE FROM moex.news WHERE symbol = ANY(%s)", (symbols,)
        )
        for symbol, publisher, title, summary, description, url in NEWS:
            data = {
                "id": url.rsplit("/", 1)[-1],
                "content": {
                    "contentType": "STORY",
                    "title": title,
                    "summary": summary,
                    "description": description,
                    "pubDate": PUBLISH_ISO,
                    "provider": {"displayName": publisher},
                    "canonicalUrl": {"url": url},
                },
            }
            cur.execute(
                "INSERT INTO moex.news (symbol, data) VALUES (%s, %s::jsonb)",
                (symbol, json.dumps(data, ensure_ascii=False)),
            )
    conn.commit()
    print(f"[preprocess] Засеяно {len(NEWS)} новостей в moex.news для тикеров {symbols}")


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--agent_workspace", required=False)
    parser.add_argument("--launch_time", required=False)
    args = parser.parse_args()

    conn = psycopg2.connect(**DB)
    try:
        clear_tables(conn)
        seed_email_folders(conn)
        seed_news(conn)
    finally:
        conn.close()

    # Копируем initial_workspace в рабочую директорию агента
    if args.agent_workspace:
        initial_ws = os.path.abspath(INITIAL_WORKSPACE)
        agent_ws = args.agent_workspace
        os.makedirs(agent_ws, exist_ok=True)
        if os.path.exists(initial_ws):
            for fname in os.listdir(initial_ws):
                src = os.path.join(initial_ws, fname)
                if os.path.isfile(src):
                    shutil.copy2(src, os.path.join(agent_ws, fname))
            print(f"[preprocess] Скопированы файлы initial_workspace в {agent_ws}")

    print("[preprocess] Готово.")


if __name__ == "__main__":
    main()
