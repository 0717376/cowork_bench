"""Evaluation for sf-ticket-escalation-report (russified: ClickHouse + Teamly).

Two-stage gate:
  1. CRITICAL_CHECKS — semantic correctness of the core deliverables.
     ANY critical failure => sys.exit(1) (immediate FAIL), regardless of accuracy.
  2. Accuracy gate — all remaining (non-critical) structural/numeric checks are
     accumulated; PASS requires accuracy >= 70 AND no critical failure.

Realia (Reporter / Issue_Type) are russified centrally in sf_data by
db/zzz_clickhouse_after_init.sql; the groundtruth xlsx was regenerated by the
same deterministic substitution, so string keys compare RU-to-RU. normalize()
is used ONLY to absorb cyrillic/latin lookalikes, never for RU keyword greps.
"""
import argparse
import os
import sys
import unicodedata

import openpyxl
import psycopg2

ACCURACY_THRESHOLD = 70.0


def normalize(s):
    """Lowercase + NFKD + collapse common cyrillic/latin lookalikes.

    Use ONLY for mixed cyr/lat identifier matching (e.g. 752А vs 752A). Do NOT
    use for RU keyword/substring detection -- those go on .lower() originals.
    """
    if s is None:
        return ""
    s = unicodedata.normalize("NFKD", str(s)).lower()
    table = str.maketrans({
        "а": "a", "о": "o", "е": "e", "р": "p", "с": "c", "у": "y", "х": "x",
        "к": "k", "м": "m", "т": "t", "н": "h", "в": "b",
    })
    return s.translate(table).strip()


def num_close(a, b, tol=1.0):
    if a is None and b is None:
        return True
    if a is None or b is None:
        return False
    try:
        return abs(float(a) - float(b)) <= tol
    except (TypeError, ValueError):
        return str(a).strip().lower() == str(b).strip().lower()


def str_match(a, b):
    """RU-to-RU / EN-to-EN equality tolerant of cyr/lat lookalikes."""
    if a is None or b is None:
        return a is None and b is None
    return normalize(a) == normalize(b)


def load_sheet_rows(wb, sheet_name):
    for name in wb.sheetnames:
        if name.strip().lower() == sheet_name.strip().lower():
            return [[cell.value for cell in row] for row in wb[name].iter_rows()]
    return None


def db_config():
    return {
        "host": os.environ.get("PGHOST", "localhost"), "port": 5432,
        "dbname": os.environ.get("PGDATABASE", "cowork_gym"),
        "user": "eigent", "password": "camel",
    }


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--agent_workspace", required=False)
    parser.add_argument("--groundtruth_workspace", required=False)
    parser.add_argument("--launch_time", required=False)
    parser.add_argument("--res_log_file", required=False)
    args = parser.parse_args()

    task_root = os.path.abspath(os.path.join(os.path.dirname(__file__), ".."))
    gt_dir = args.groundtruth_workspace or os.path.join(task_root, "groundtruth_workspace")

    agent_file = os.path.join(args.agent_workspace, "Escalation_Report.xlsx")
    gt_file = os.path.join(gt_dir, "Escalation_Report.xlsx")

    if not os.path.exists(agent_file):
        print(f"FAIL: Agent output not found: {agent_file}")
        sys.exit(1)
    if not os.path.exists(gt_file):
        print(f"FAIL: Groundtruth not found: {gt_file}")
        sys.exit(1)

    agent_wb = openpyxl.load_workbook(agent_file, data_only=True)
    gt_wb = openpyxl.load_workbook(gt_file, data_only=True)

    critical_failures = []   # any entry => immediate FAIL
    checks = []              # (label, passed) -> accuracy-counted

    def record(label, passed, detail=""):
        checks.append((label, bool(passed)))
        status = "PASS" if passed else "FAIL"
        print(f"    [{status}] {label}" + (f" :: {detail}" if detail and not passed else ""))

    def critical(label, passed, detail=""):
        if not passed:
            critical_failures.append(f"{label} :: {detail}")
        status = "PASS" if passed else "FAIL"
        print(f"    [CRITICAL {status}] {label}" + (f" :: {detail}" if detail and not passed else ""))

    # ── Helpers to pull single values out of Summary sheet ───────────────────
    def summary_lookup(wb):
        rows = load_sheet_rows(wb, "Summary")
        if not rows:
            return {}
        out = {}
        for r in rows[1:]:
            if r and r[0] is not None:
                out[str(r[0]).strip().lower()] = r[1] if len(r) > 1 else None
        return out

    a_sum = summary_lookup(agent_wb)
    g_sum = summary_lookup(gt_wb)

    # =====================================================================
    # CRITICAL CHECKS (semantic substance)
    # =====================================================================
    print("\n=== CRITICAL CHECKS ===")

    # C1: Total_Tickets matches groundtruth tightly -> proves full warehouse query.
    critical("Summary.Total_Tickets matches groundtruth",
             num_close(a_sum.get("total_tickets"), g_sum.get("total_tickets"), 5),
             f"{a_sum.get('total_tickets')} vs {g_sum.get('total_tickets')}")

    # C2: Low satisfaction count + pct -> central low-CSAT deliverable.
    critical("Summary.Low_Satisfaction_Tickets matches groundtruth",
             num_close(a_sum.get("low_satisfaction_tickets"),
                       g_sum.get("low_satisfaction_tickets"), 20),
             f"{a_sum.get('low_satisfaction_tickets')} vs {g_sum.get('low_satisfaction_tickets')}")
    critical("Summary.Low_Satisfaction_Pct matches groundtruth",
             num_close(a_sum.get("low_satisfaction_pct"),
                       g_sum.get("low_satisfaction_pct"), 0.5),
             f"{a_sum.get('low_satisfaction_pct')} vs {g_sum.get('low_satisfaction_pct')}")

    # C3: Worst issue type (RU-to-RU; normalize absorbs cyr/lat lookalikes).
    critical("Summary.Worst_Issue_Type matches groundtruth",
             str_match(a_sum.get("worst_issue_type"), g_sum.get("worst_issue_type")),
             f"{a_sum.get('worst_issue_type')} vs {g_sum.get('worst_issue_type')}")

    # C4: Priority Summary per-priority aggregation (Low/Medium/High).
    a_ps = load_sheet_rows(agent_wb, "Priority Summary") or []
    g_ps = load_sheet_rows(gt_wb, "Priority Summary") or []
    a_ps_lookup = {str(r[0]).strip().lower(): r for r in a_ps[1:] if r and r[0] is not None}
    g_ps_lookup = {str(r[0]).strip().lower(): r for r in g_ps[1:] if r and r[0] is not None}
    ps_ok = True
    ps_detail = ""
    for key in ("low", "medium", "high"):
        g_row = g_ps_lookup.get(key)
        a_row = a_ps_lookup.get(key)
        if g_row is None:
            continue
        if a_row is None:
            ps_ok = False
            ps_detail = f"missing priority {key}"
            break
        # Total_Tickets (col 1, tight) and Avg_CSAT (col 5, 0.1)
        if len(g_row) > 1 and not num_close(a_row[1], g_row[1], 30):
            ps_ok = False
            ps_detail = f"{key}.Total_Tickets {a_row[1]} vs {g_row[1]}"
            break
        if len(g_row) > 5 and not num_close(a_row[5], g_row[5], 0.1):
            ps_ok = False
            ps_detail = f"{key}.Avg_CSAT {a_row[5]} vs {g_row[5]}"
            break
    critical("Priority Summary per-priority aggregation (Low/Medium/High)",
             ps_ok, ps_detail)

    # C5: Escalation email exists AND addressed to escalations@company.com.
    try:
        conn = psycopg2.connect(**db_config())
        cur = conn.cursor()
        cur.execute("""
            SELECT subject, to_addr, body_text FROM email.messages
            WHERE subject ILIKE '%escalation%'
        """)
        emails = cur.fetchall()
        cur.close()
        conn.close()
        has_email = len(emails) >= 1
        to_ok = any("escalations@company.com" in (str(t or "").lower()) for _, t, _ in emails)
        critical("Escalation email exists addressed to escalations@company.com",
                 has_email and to_ok,
                 f"emails={len(emails)} to_match={to_ok}")
    except Exception as e:
        critical("Escalation email exists addressed to escalations@company.com", False, str(e))

    # If any critical failed -> FAIL immediately.
    if critical_failures:
        print(f"\n=== RESULT: FAIL ({len(critical_failures)} critical failures) ===")
        for f in critical_failures:
            print(f"  CRITICAL: {f}")
        sys.exit(1)

    # =====================================================================
    # NON-CRITICAL (accuracy-counted) STRUCTURAL + NUMERIC CHECKS
    # =====================================================================
    print("\n=== STRUCTURAL / NUMERIC CHECKS ===")

    # ── At Risk Tickets sheet (newly covered) ────────────────────────────
    print("  At Risk Tickets...")
    a_rows = load_sheet_rows(agent_wb, "At Risk Tickets")
    g_rows = load_sheet_rows(gt_wb, "At Risk Tickets")
    record("Sheet 'At Risk Tickets' present", a_rows is not None)
    if a_rows is not None and g_rows is not None:
        a_lookup = {str(r[0]).strip().lower(): r for r in a_rows[1:] if r and r[0] is not None}
        for g_row in g_rows[1:]:
            if not g_row or g_row[0] is None:
                continue
            key = str(g_row[0]).strip().lower()
            a_row = a_lookup.get(key)
            record(f"At Risk Tickets[{key}] present", a_row is not None)
            if a_row is None:
                continue
            if len(g_row) > 1:
                record(f"At Risk Tickets[{key}].Total_Tickets",
                       num_close(a_row[1], g_row[1], 50),
                       f"{a_row[1]} vs {g_row[1]}")
            if len(g_row) > 4:
                record(f"At Risk Tickets[{key}].Avg_Response_Hours",
                       num_close(a_row[4], g_row[4], 1.0),
                       f"{a_row[4]} vs {g_row[4]}")
            if len(g_row) > 5:
                record(f"At Risk Tickets[{key}].SLA_Hours",
                       num_close(a_row[5], g_row[5], 0.5),
                       f"{a_row[5]} vs {g_row[5]}")

    # ── Low Satisfaction sheet (newly covered) ───────────────────────────
    print("  Low Satisfaction...")
    a_rows = load_sheet_rows(agent_wb, "Low Satisfaction")
    g_rows = load_sheet_rows(gt_wb, "Low Satisfaction")
    record("Sheet 'Low Satisfaction' present", a_rows is not None)
    if a_rows is not None and g_rows is not None:
        # Key on (Priority, Issue_Type) using normalize for cyr/lat tolerance.
        a_lookup = {}
        for r in a_rows[1:]:
            if r and r[0] is not None and len(r) > 1:
                a_lookup[(normalize(r[0]), normalize(r[1]))] = r
        # Sample top rows of groundtruth to bound check volume.
        for g_row in g_rows[1:]:
            if not g_row or g_row[0] is None or len(g_row) < 3:
                continue
            key = (normalize(g_row[0]), normalize(g_row[1]))
            a_row = a_lookup.get(key)
            label = f"Low Satisfaction[{g_row[0]}/{g_row[1]}]"
            record(f"{label} present", a_row is not None)
            if a_row is None:
                continue
            if len(g_row) > 2:
                record(f"{label}.Low_CSAT_Count",
                       num_close(a_row[2], g_row[2], 30),
                       f"{a_row[2]} vs {g_row[2]}")

    # ── Priority Summary (remaining numeric detail, non-critical) ─────────
    print("  Priority Summary (detail)...")
    record("Sheet 'Priority Summary' present", bool(a_ps))
    for key in ("low", "medium", "high", "critical"):
        g_row = g_ps_lookup.get(key)
        if g_row is None:
            continue
        a_row = a_ps_lookup.get(key)
        record(f"Priority Summary[{key}] present", a_row is not None)
        if a_row is None:
            continue
        if len(g_row) > 2:
            record(f"Priority Summary[{key}].Low_CSAT_Tickets",
                   num_close(a_row[2], g_row[2], 50),
                   f"{a_row[2]} vs {g_row[2]}")
        if len(g_row) > 3:
            record(f"Priority Summary[{key}].Low_CSAT_Pct",
                   num_close(a_row[3], g_row[3], 1.0),
                   f"{a_row[3]} vs {g_row[3]}")
        if len(g_row) > 4:
            record(f"Priority Summary[{key}].Avg_Response_Hours",
                   num_close(a_row[4], g_row[4], 1.0),
                   f"{a_row[4]} vs {g_row[4]}")
        if len(g_row) > 6:
            record(f"Priority Summary[{key}].SLA_Hours",
                   num_close(a_row[6], g_row[6], 0.5),
                   f"{a_row[6]} vs {g_row[6]}")

    # ── Summary sheet (remaining metrics, non-critical) ──────────────────
    print("  Summary (detail)...")
    g_rows = load_sheet_rows(gt_wb, "Summary")
    if g_rows:
        for g_row in g_rows[1:]:
            if not g_row or g_row[0] is None:
                continue
            key = str(g_row[0]).strip().lower()
            if key in ("total_tickets", "low_satisfaction_tickets",
                       "low_satisfaction_pct", "worst_issue_type"):
                continue  # covered by critical checks
            a_val = a_sum.get(key)
            g_val = g_row[1] if len(g_row) > 1 else None
            if "issue_type" in key or "priority" in key:
                record(f"Summary.{key}", str_match(a_val, g_val), f"{a_val} vs {g_val}")
            elif "pct" in key:
                record(f"Summary.{key}", num_close(a_val, g_val, 1.0), f"{a_val} vs {g_val}")
            elif "hours" in key or "satisfaction" in key or "csat" in key:
                record(f"Summary.{key}", num_close(a_val, g_val, 0.5), f"{a_val} vs {g_val}")
            else:
                record(f"Summary.{key}", num_close(a_val, g_val, 100), f"{a_val} vs {g_val}")

    # ── Reporter Analysis (RU realia, non-critical) ──────────────────────
    print("  Reporter Analysis...")
    a_rows = load_sheet_rows(agent_wb, "Reporter Analysis")
    g_rows = load_sheet_rows(gt_wb, "Reporter Analysis")
    record("Sheet 'Reporter Analysis' present", a_rows is not None)
    if a_rows is not None and g_rows is not None:
        a_lookup = {normalize(r[0]): r for r in a_rows[1:] if r and r[0] is not None}
        for g_row in g_rows[1:]:
            if not g_row or g_row[0] is None:
                continue
            key = normalize(g_row[0])
            a_row = a_lookup.get(key)
            record(f"Reporter Analysis[{g_row[0]}] present", a_row is not None)
            if a_row is None:
                continue
            if len(g_row) > 1:
                record(f"Reporter Analysis[{g_row[0]}].Total_Tickets",
                       num_close(a_row[1], g_row[1], 50),
                       f"{a_row[1]} vs {g_row[1]}")
            if len(g_row) > 5:
                record(f"Reporter Analysis[{g_row[0]}].Avg_CSAT",
                       num_close(a_row[5], g_row[5], 0.1),
                       f"{a_row[5]} vs {g_row[5]}")

    # ── Teamly escalation page (substance, non-critical) ─────────────────
    print("  Teamly page...")
    try:
        conn = psycopg2.connect(**db_config())
        cur = conn.cursor()
        cur.execute("""
            SELECT title, body FROM teamly.pages
            WHERE title ILIKE '%escalation%' OR title ILIKE '%эскалац%'
               OR body ILIKE '%escalation%' OR body ILIKE '%эскалац%'
        """)
        pages = cur.fetchall()
        cur.close()
        conn.close()
        record("Teamly escalation page exists", len(pages) >= 1)
        if pages:
            blob = " ".join((str(t or "") + " " + str(b or "")) for t, b in pages).lower()
            # Substantive body length.
            max_len = max(len(str(b or "")) for _, b in pages)
            record("Teamly page has substantive body (>120 chars)", max_len > 120,
                   f"max_len={max_len}")
            # Action items present (RU or EN keyword).
            action_kw = any(k in blob for k in (
                "action", "действ", "рекоменд", "пункт", "шаг"))
            record("Teamly page mentions action items", action_kw)
    except Exception as e:
        record("Teamly escalation page exists", False, str(e))

    # =====================================================================
    # ACCURACY GATE
    # =====================================================================
    total = len(checks)
    passed = sum(1 for _, ok in checks if ok)
    accuracy = (passed / total * 100.0) if total else 0.0

    print(f"\n=== Non-critical checks: {passed}/{total} passed "
          f"({accuracy:.1f}%) ===")

    if accuracy >= ACCURACY_THRESHOLD:
        print("=== RESULT: PASS ===")
        sys.exit(0)
    else:
        print(f"=== RESULT: FAIL (accuracy {accuracy:.1f}% < {ACCURACY_THRESHOLD}) ===")
        sys.exit(1)


if __name__ == "__main__":
    main()
